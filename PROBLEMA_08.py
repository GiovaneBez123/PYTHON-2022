# Desenvolva um algoritmo e um fluxograma para um programa que permita com que o usuário insira quatro números.
# O programa deverá imprimir a média ponderada, sabendo-se que os pesos são respectivamente: 1, 2, 3 e 4.

valor1 = float(input("digite um numero1:"))
valor2 = float(input("digite um numero2:"))
valor3 = float(input("digite um numero3:"))
valor4 = float(input("digite um numero4:"))

calculo = (valor1 + valor2 + valor3 + valor4)/10

print("o valor da média é:",calculo)
